export function transliterate(text: string): string {
  const transliterationRules: Record<string, string> = {
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I',
    'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T',
    'У': 'U', 'Ф': 'F', 'Х': 'Kh', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sh', 'Ъ': '', 'Ы': 'Y', 'Ь': '',
    'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
    'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
    'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sh', 'ъ': '', 'ы': 'y', 'ь': '',
    'э': 'e', 'ю': 'yu', 'я': 'ya',
    ' ': '-',
    ',': '',
    '.': '',
    '!': '',
    '?': '',
    ':': '',
    ';': '',
    '"': '',
    "'": '',
    '(': '',
    ')': '',
    '/': '-',
    '\\': '-'
  }

  let result = ''
  for (let i = 0; i < text.length; i++) {
    const ch = text[i]
    if (transliterationRules.hasOwnProperty(ch)) {
      result += transliterationRules[ch]
    } else if (/[a-zA-Z0-9-]/.test(ch)) {
      result += ch
    }
  }
  
  // Убираем множественные дефисы и приводим к нижнему регистру
  return result.toLowerCase().replace(/-+/g, '-').replace(/^-|-$/g, '')
}
